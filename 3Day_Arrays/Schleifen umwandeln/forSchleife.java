public class forSchleife {
    public static void main(String[] args) {
        int a = 0;
      
		do{
			a += 2;
			System.out.println(a);
        } while (a < 7);
        
        System.out.println();

        for (int i = 0; i <= 7;) {
            i += 2;
            System.out.println(i);
        }

    }
}
